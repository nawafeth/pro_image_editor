/// Defines the available editor modes.
enum EditorMode {
  /// The main editor.
  main,

  /// The paint editor.
  paint,

  /// The text editor.
  text,

  /// The crop & rotate editor.
  cropRotate,

  /// The tune editor.
  tune,

  /// The filter editor.
  filter,

  /// The blur editor.
  blur,

  /// The emoji editor.
  emoji,

  /// The sticker editor.
  sticker,
}

/// Defines the available sub-editor modes.
enum SubEditorMode {
  /// The paint editor.
  paint,

  /// The text editor.
  text,

  /// The crop & rotate editor.
  cropRotate,

  /// The tune editor.
  tune,

  /// The filter editor.
  filter,

  /// The blur editor.
  blur,

  /// The emoji editor.
  emoji,

  /// The sticker editor.
  sticker,

  /// The video-audio editor.
  audio,

  /// The video-clips editor.
  videoClips,
}
